-- Alter puntual para bases NEXOPAGODB.FDB ya creadas antes de este cambio.
-- Ejecutar una sola vez contra la base real (SYSDBA). Ver nexopago_db.sql
-- para el esquema completo ya actualizado (instalaciones nuevas).
--
-- Contexto: GetCodigoEmpresaHelisa (NexoPago.Helisa.Connection.pas) leia el
-- codigo de empresa Helisa activa desde un INI local (THConfig), sin
-- trazabilidad ni auditoria. Se traslada a la base de NexoPago:
-- EMPRESA_ACTIVA guarda la fila unica vigente (UNIQUE + CHECK sobre
-- SINGLETON_LOCK garantizan que nunca haya mas de una), EMPRESA_ACTIVA_HISTORIAL
-- registra cada cambio para auditoria. Tambien agrega el modulo/permiso
-- CONFIGURACION.CAMBIAR_EMPRESA usado por el nuevo endpoint
-- PUT /api/empresa/activa.

CREATE TABLE EMPRESA_ACTIVA (
    EMPRESA_ACTIVA_ID     INTEGER GENERATED BY DEFAULT AS IDENTITY,
    CODIGO_EMPRESA_HELISA INTEGER NOT NULL,
    NOMBRE_EMPRESA        VARCHAR(100) CHARACTER SET ISO8859_1 NOT NULL,
    SINGLETON_LOCK        CHAR(1) CHARACTER SET ISO8859_1 DEFAULT 'X' NOT NULL,
    USUARIO_CREO_ID       INTEGER,
    FECHA_CREACION        TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    USUARIO_MODIFICO_ID   INTEGER,
    FECHA_MODIFICACION    TIMESTAMP,
    "VERSION"             INTEGER DEFAULT 0
);

ALTER TABLE EMPRESA_ACTIVA ADD PRIMARY KEY (EMPRESA_ACTIVA_ID);
ALTER TABLE EMPRESA_ACTIVA ADD UNIQUE (SINGLETON_LOCK);
ALTER TABLE EMPRESA_ACTIVA ADD CONSTRAINT CHK1_EMPRESA_ACTIVA CHECK (SINGLETON_LOCK = 'X');
ALTER TABLE EMPRESA_ACTIVA ADD CONSTRAINT FK_EMPRESA_ACTIVA_1 FOREIGN KEY (USUARIO_CREO_ID) REFERENCES USUARIO (USUARIO_ID);
ALTER TABLE EMPRESA_ACTIVA ADD CONSTRAINT FK_EMPRESA_ACTIVA_2 FOREIGN KEY (USUARIO_MODIFICO_ID) REFERENCES USUARIO (USUARIO_ID);

COMMENT ON TABLE EMPRESA_ACTIVA IS
'Empresa Helisa activa a la que esta conectado NexoPago (fila unica, ver SINGLETON_LOCK)';
COMMENT ON COLUMN EMPRESA_ACTIVA.CODIGO_EMPRESA_HELISA IS
'Codigo de empresa de Helisa (DIRECTOR.CODIGO) al que esta conectado NexoPago';
COMMENT ON COLUMN EMPRESA_ACTIVA.SINGLETON_LOCK IS
'Siempre ''X''; el UNIQUE + CHECK sobre esta columna garantiza una sola fila en la tabla';


CREATE TABLE EMPRESA_ACTIVA_HISTORIAL (
    HISTORIAL_ID             INTEGER GENERATED BY DEFAULT AS IDENTITY,
    CODIGO_EMPRESA_ANTERIOR  INTEGER,
    NOMBRE_EMPRESA_ANTERIOR  VARCHAR(100) CHARACTER SET ISO8859_1,
    CODIGO_EMPRESA_NUEVA     INTEGER NOT NULL,
    NOMBRE_EMPRESA_NUEVA     VARCHAR(100) CHARACTER SET ISO8859_1 NOT NULL,
    USUARIO_ID               INTEGER NOT NULL,
    FECHA_CAMBIO             TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE EMPRESA_ACTIVA_HISTORIAL ADD PRIMARY KEY (HISTORIAL_ID);
ALTER TABLE EMPRESA_ACTIVA_HISTORIAL ADD CONSTRAINT FK_EMPRESA_ACTIVA_HIST_1 FOREIGN KEY (USUARIO_ID) REFERENCES USUARIO (USUARIO_ID);
CREATE DESCENDING INDEX IDX_EMPRESA_ACTIVA_HIST_FECHA ON EMPRESA_ACTIVA_HISTORIAL (FECHA_CAMBIO);

COMMENT ON TABLE EMPRESA_ACTIVA_HISTORIAL IS
'Historial de cambios de la empresa Helisa activa (auditoria de EMPRESA_ACTIVA)';
COMMENT ON COLUMN EMPRESA_ACTIVA_HISTORIAL.CODIGO_EMPRESA_ANTERIOR IS
'Codigo de empresa Helisa antes del cambio; NULL en el primer cambio (no habia configuracion previa)';
COMMENT ON COLUMN EMPRESA_ACTIVA_HISTORIAL.CODIGO_EMPRESA_NUEVA IS
'Codigo de empresa Helisa despues del cambio';


-- Seed: modulo CONFIGURACION + permiso CAMBIAR_EMPRESA, usado por
-- TPermisosService.UsuarioTienePermiso en PUT /api/empresa/activa. Sin este
-- seed nadie tiene el permiso (hay que asignarlo a un perfil desde la
-- pantalla de Permisos despues de correr este script).
INSERT INTO MODULO (NOMBRE, DESCRIPCION, ACTIVO) VALUES ('CONFIGURACION', 'Configuracion general del sistema', 1);
INSERT INTO PERMISO (MODULO_ID, ACCION, DESCRIPCION)
  VALUES ((SELECT MODULO_ID FROM MODULO WHERE NOMBRE = 'CONFIGURACION'), 'CAMBIAR_EMPRESA', 'Cambiar la empresa Helisa activa');

COMMIT;
