-- 4) Asignar TODOS los permisos recien insertados al perfil ADMINISTRADOR (1).
-- No toca los permisos 1-4 (ya estaban asignados desde antes) ni asigna nada
-- a OPERADOR_COMPRA (2).
INSERT INTO PERFIL_PERMISO (PERFIL_ID, PERMISO_ID)
  SELECT 1, P.PERMISO_ID
  FROM PERMISO P
  WHERE P.ACCION IN (
    'ORDENES_EDITAR', 'ORDENES_ANULAR',
    'ENTRADAS_LEER', 'ENTRADAS_REGISTRAR',
    'PROVEEDORES_LEER', 'PROVEEDORES_CREAR', 'PROVEEDORES_EDITAR', 'PROVEEDORES_ELIMINAR',
    'RECIBOS_LEER', 'RECIBOS_CREAR', 'RECIBOS_ANULAR',
    'PRODUCTOS_LEER', 'PRODUCTOS_SINCRONIZAR',
    'USUARIOS_CREAR', 'USUARIOS_EDITAR', 'USUARIOS_ESTADO',
    'PERMISOS_LEER', 'PERMISOS_ASIGNAR'
  )
  AND NOT EXISTS (
    SELECT 1 FROM PERFIL_PERMISO PP
    WHERE PP.PERFIL_ID = 1 AND PP.PERMISO_ID = P.PERMISO_ID
  );

COMMIT;
