-- Alter puntual para bases NEXOPAGODB.FDB ya creadas antes de este cambio.
-- Ejecutar una sola vez contra la base real (SYSDBA). Ver nexopago_db.sql
-- para el esquema completo (instalaciones nuevas deberian incorporar este
-- mismo contenido alli).
--
-- Contexto: OnAuthorization (NexoPago.Services.Auth.pas) siempre devolvia
-- AIsAuthorized := True -solo habia autenticacion, no autorizacion real por
-- permiso-. Este cambio activa el atributo [TMVCRequiresPermiso(modulo, accion)]
-- sobre las acciones de los controllers, verificado via RTTI en
-- OnAuthorization contra IPermisoRepository.UsuarioTienePermiso (el mismo
-- mecanismo que ya usaba TEmpresaService.CambiarEmpresaActiva). Para que la
-- autorizacion real no rompa el acceso, este script:
--   1) Amplia PERMISO.ACCION (20 -> 40 caracteres): codigos nuevos como
--      PROVEEDORES_ELIMINAR no caben en VARCHAR(20).
--   2) Renombra los 3 permisos existentes (por PERMISO_ID, sin tocar
--      PERFIL_PERMISO) a codigos mas descriptivos por recurso.
--   3) Inserta todos los permisos granulares nuevos que exige el mapeo de
--      controllers -> [TMVCRequiresPermiso].
--   4) Asigna TODOS los permisos nuevos al perfil ADMINISTRADOR (perfil 1),
--      para que el admin no pierda acceso a nada al empezar a exigirse la
--      autorizacion de verdad. OPERADOR_COMPRA (perfil 2) NO recibe nada
--      nuevo aqui: esa asignacion se decide manualmente despues desde la
--      pantalla de Permisos.
--
-- Idempotencia: los UPDATE de renombrado son idempotentes (si ya se
-- corrieron, el segundo UPDATE no encuentra el valor viejo y no hace nada).
-- Los INSERT de permisos/asignaciones usan NOT EXISTS para poder re-correr
-- el script sin duplicar filas.

-- 1) Ampliar PERMISO.ACCION
-- El UNIQUE (MODULO_ID, ACCION) (UQ_PERMISO_MODULO_ACCION) usa un indice
-- sobre ACCION; Firebird no permite ALTER COLUMN TYPE mientras ese indice
-- exista (SQLSTATE 27000, "Cannot update index segment used by an Integrity
-- Constraint"). Se elimina antes del ALTER y se recrea despues, idéntico.
ALTER TABLE PERMISO DROP CONSTRAINT UQ_PERMISO_MODULO_ACCION;
COMMIT;

ALTER TABLE PERMISO ALTER COLUMN ACCION TYPE VARCHAR(40);
COMMIT;

ALTER TABLE PERMISO ADD CONSTRAINT UQ_PERMISO_MODULO_ACCION UNIQUE (MODULO_ID, ACCION);
COMMIT;

-- 2) Renombrar los 3 permisos existentes (por ID, PERFIL_PERMISO no se toca)
UPDATE PERMISO SET ACCION = 'ORDENES_CREAR' WHERE PERMISO_ID = 1 AND ACCION = 'CREAR';
UPDATE PERMISO SET ACCION = 'ORDENES_LEER' WHERE PERMISO_ID = 2 AND ACCION = 'LEER';
UPDATE PERMISO SET ACCION = 'USUARIOS_LEER' WHERE PERMISO_ID = 3 AND ACCION = 'LEER';
-- PERMISO_ID = 4 (CAMBIAR_EMPRESA) no cambia.
COMMIT;

